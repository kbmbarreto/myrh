package dao;

public class Recrutadores {
	
	private String id, data_contato, nome, telefone, email, empresa, meio_de_contato;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getData_contato() {
		return data_contato;
	}

	public void setData_contato(String data_contato) {
		this.data_contato = data_contato;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getTelefone() {
		return telefone;
	}

	public void setTelefone(String telefone) {
		this.telefone = telefone;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getEmpresa() {
		return empresa;
	}

	public void setEmpresa(String empresa) {
		this.empresa = empresa;
	}

	public String getMeio_de_contato() {
		return meio_de_contato;
	}

	public void setMeio_de_contato(String meio_de_contato) {
		this.meio_de_contato = meio_de_contato;
	}
}